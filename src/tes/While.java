package tes;

public class While {

	public static void main(String[] args) {
		
		int b = 1;
		
		while (b<5) {
			
			System.out.println("El numero es igual a 1");
			
			b++;
		}
	
		System.out.println("Final del programa");
		
		
		
		
		
		

		int c = 0;
		
		while (c<=100) {
			
			System.out.println("El numero es menor e igual a 100");
			System.out.println("El valor de la variable c :" + c);
			
			c++;
		}
	
		System.out.println("Final del programa");
		
		
	}
}
